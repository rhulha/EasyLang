package net.raysforge.rayslang;

public class RayVar {

	Visibility visibility;
	String type;
	String name;
	String value;
	
	public RayVar(Visibility visibility, String type, String name, String value) {
		super();
		this.visibility = visibility;
		this.type = type;
		this.name = name;
		this.value = value;
	}

	
}
